const colors = [
  'rgba(250, 84, 0, 0.9)',   // cam
  'rgba(0, 150, 255, 0.9)',  // xanh dương
  'rgba(34, 197, 94, 0.9)',  // xanh lá
  'rgba(255, 215, 0, 0.9)',  // vàng
  'rgba(255, 105, 180, 0.9)' // hồng
];

const FallingShoes = () => {
  const [shoes, setShoes] = useState([]);

  useEffect(() => {
    const createShoe = () => {
      const id = Date.now() + Math.random();
      const shoe = {
        id,
        iconIndex: Math.floor(Math.random() * shoeIcons.length),
        left: Math.random() * 100,
        duration: 12 + Math.random() * 8,
        delay: Math.random() * 3,
        size: 20 + Math.random() * 16,
        opacity: 0.5 + Math.random() * 0.5,
        rotation: Math.random() * 360,
        color: colors[Math.floor(Math.random() * colors.length)],
      };
      setShoes(prev => [...prev, shoe]);
      setTimeout(() => {
        setShoes(prev => prev.filter(s => s.id !== id));
      }, shoe.duration * 1000);
    };

    for (let i = 0; i < 2; i++) {
      setTimeout(createShoe, i * 3000);
    }

    const interval = setInterval(createShoe, 5000);
    return () => clearInterval(interval);
  }, []);

  return (
    <div className="falling-shoes" aria-hidden="true">
      {shoes.map(shoe => (
        <span
          key={shoe.id}
          className="falling-shoes__shoe"
          style={{
            left: `${shoe.left}%`,
            animationDuration: `${shoe.duration}s`,
            animationDelay: `${shoe.delay}s`,
            opacity: shoe.opacity,
            transform: `rotate(${shoe.rotation}deg)`,
          }}
        >
          {shoeIcons[shoe.iconIndex](shoe.size, shoe.color)}
        </span>
      ))}
    </div>
  );
};

export default FallingShoes;